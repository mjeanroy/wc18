package com.github.mjeanroy.wc18.api.dto;

/**
 * Abstract structure for DTO entities.
 */
public class AbstractDto {

	/**
	 * The DTO id.
	 */
	private String id;

	/**
	 * Get {@link #id}
	 *
	 * @return {@link #id}
	 */
	public String getId() {
		return id;
	}

	/**
	 * Set {@link #id}
	 *
	 * @param id New {@link #id}
	 */
	public void setId(String id) {
		this.id = id;
	}
}
